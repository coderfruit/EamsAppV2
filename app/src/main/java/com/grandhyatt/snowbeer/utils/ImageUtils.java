    /**
     * byte数组转图片
     * @param b
     * @return
     */
    public static Bitmap Bytes2Bimap(byte[] b) {
        Bitmap btp = null;
        if (b.length != 0) {
            btp = BitmapFactory.decodeByteArray(b, 0, b.length);
        }
        return btp;
    }

    /**
     * 图片转byte[]
     * @param bitmap
     * @return
     */
    public static byte[] Bitmap2Byte(Bitmap bitmap)
    {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        bitmap.compress(Bitmap.CompressFormat.JPEG, 100, baos);
        byte[] datas = baos.toByteArray();
        return datas;
    }

    // 将InputStream转换成Bitmap
    public static Bitmap InputStream2Bitmap(InputStream is) {
        // BitmapFactory.Options opts = new BitmapFactory.Options();
        // opts.inSampleSize = 2;
        return BitmapFactory.decodeStream(is);
    }

    public static Bitmap base64ToBitmap(String base64Data) {
        byte[] bytes = Base64.decode(base64Data, Base64.DEFAULT);
        return BitmapFactory.decodeByteArray(bytes, 0, bytes.length);
    }

    // 将Bitmap转换成InputStream
    public static InputStream Bitmap2InputStream(Bitmap bm) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        bm.compress(Bitmap.CompressFormat.JPEG, 75, baos);
        InputStream is = new ByteArrayInputStream(baos.toByteArray());
        return is;
    }

    // 将Bitmap转换成InputStream
    public static InputStream Bitmap2InputStream(Bitmap bm, int quality) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        bm.compress(Bitmap.CompressFormat.PNG, quality, baos);
        InputStream is = new ByteArrayInputStream(baos.toByteArray());
        return is;
    }

    /**
     * 将图片转换成Base64编码的字符串
     *
     * @param path
     * @return base64编码的字符串
     */
    public static String imageToBase64(String path) {
        if (TextUtils.isEmpty(path)) {
            return null;
        }
        InputStream is = null;
        byte[] data = null;
        String result = null;
        try {
            //is = new FileInputStream(path);

//            Bitmap bmp = compressImg(path);//压缩图片
            Bitmap bmp = compressSize(path);//压缩图片
            is = Bitmap2InputStream(bmp);//图片转为输入流

            //创建一个字符流大小的数组。
            data = new byte[is.available()];
            //写入数组
            is.read(data);
            //用默认的编码格式进行编码
            result = Base64.encodeToString(data, Base64.DEFAULT);
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (null != is) {
                try {
                    is.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
        return result;
    }

    /**
     * list转byte数组
     *
     * @param list
     * @return
     */
    public static byte[] ListToByteArray(java.util.List<Object> list) {
        if (list == null || list.size() < 0)
            return null;
        byte[] bytes = new byte[list.size()];
        int i = 0;
        Iterator<Object> iterator = list.iterator();
        while (iterator.hasNext()) {
            bytes[i] = (byte) iterator.next();
            i++;
        }
        return bytes;
    }

    /**
     * 压缩图片
     *
     * @param itemUrl
     * @return
     */
    public static Bitmap compressImg(String itemUrl) {
        BitmapFactory.Options opts = new BitmapFactory.Options();
        opts.inSampleSize = 2;    //这个的值压缩的倍数（2的整数倍），数值越小，压缩率越小，图片越清晰
        Bitmap bitmap = BitmapFactory.decodeFile(itemUrl, opts);//返回原图解码之后的bitmap对象
        return bitmap;
    }


    /**
     * 图片按比例大小压缩方法
     *
     * @param btBmp （根据byte[]获取图片并压缩）
     * @return
     */
    public static Bitmap compressSize(byte[] btBmp) {

        BitmapFactory.Options newOpts = new BitmapFactory.Options();
        // 开始读入图片，此时把options.inJustDecodeBounds 设回true了
        newOpts.inJustDecodeBounds = true;
        Bitmap bitmap = BitmapFactory.decodeByteArray(btBmp,0,btBmp.length, newOpts);// 此时返回bm为空
        newOpts.inJustDecodeBounds = false;
        int w = newOpts.outWidth;
        int h = newOpts.outHeight;
        // 现在主流手机比较多是800*480分辨率，所以高和宽我们设置为
        float hh = 800f;// 这里设置高度为800f
        float ww = 480f;// 这里设置宽度为480f
        // 缩放比。由于是固定比例缩放，只用高或者宽其中一个数据进行计算即可
        int be = 1;// be=1表示不缩放
        if (w > h && w > ww) {// 如果宽度大的话根据宽度固定大小缩放
            be = (int) (newOpts.outWidth / ww);
        } else if (w < h && h > hh) {// 如果高度高的话根据宽度固定大小缩放
            be = (int) (newOpts.outHeight / hh);
        }
        if (be <= 0)
            be = 1;
        newOpts.inSampleSize = be;// 设置缩放比例
        // 重新读入图片，注意此时已经把options.inJustDecodeBounds 设回false了
        bitmap = BitmapFactory.decodeByteArray(btBmp,0,btBmp.length, newOpts);
        return compressImage(bitmap);// 压缩好比例大小后再进行质量压缩
    }
